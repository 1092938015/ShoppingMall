package po;

public class CommodityType {
    private Integer CommodityType_ID;
    private String CommodityType_Name;
    private String CommodityType_Description;
    private String CommodityType_Icon;

    public Integer getCommodityType_ID() {
        return CommodityType_ID;
    }

    public void setCommodityType_ID(Integer commodityType_ID) {
        CommodityType_ID = commodityType_ID;
    }

    public String getCommodityType_Name() {
        return CommodityType_Name;
    }

    public void setCommodityType_Name(String commodityType_Name) {
        CommodityType_Name = commodityType_Name;
    }

    public String getCommodityType_Description() {
        return CommodityType_Description;
    }

    public void setCommodityType_Description(String commodityType_Description) {
        CommodityType_Description = commodityType_Description;
    }

    public String getCommodityType_Icon() {
        return CommodityType_Icon;
    }

    public void setCommodityType_Icon(String commodityType_Icon) {
        CommodityType_Icon = commodityType_Icon;
    }
}
